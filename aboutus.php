      <h1>Introduction</h1>
      <p>The project of Library Management System was developed as an attempt to blend the traditional system of Libraries and today's Technology. It is a unique combination of Web Technologies and Database Management. Traditionally, it is a very tedious task to keep a log of all the books present in the library and track all the people who have issued books. This also has several limitations. The digital version of a Library Management System allows the librarian to do all of the things in a computerised manner. The system reduces the workload of the staff and the manual work is greatly reduced.</p>
